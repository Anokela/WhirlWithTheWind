

public class PowerUps
{
    private static int downDashPrice;
    private static int upDashPrice;
    private static int sideDashPrice;
    public static int DownDashPrice
    {
        get
        {
            return downDashPrice;
        }
        set
        {
            downDashPrice = value;
        }
    }
    public static int UpDashPrice
    {
        get
        {
            return upDashPrice;
        }
        set
        {
            upDashPrice = value;
        }
    }
    public static int SideDashPrice
    {
        get
        {
            return sideDashPrice;
        }
        set
        {
            sideDashPrice = value;
        }
    }
}
